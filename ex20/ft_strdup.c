#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	int		length;
	char	*duplicate;
	int		i;

	length = ft_strlen(src);
	i = 0;
	duplicate = malloc((length +1) * sizeof(char));
	if (duplicate == NULL)
		return (NULL);
	while (src[i] != '\0')
	{
		duplicate[i] = src[i];
		i++;
	}
	duplicate[i] = '\0';
	return (duplicate);
}
